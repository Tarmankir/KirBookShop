public class Seller {

    private String name;
    private String workingDay;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setWorkingDay(String workingDay) {
        this.workingDay = workingDay;
    }

    public String getWorkingDay() {
        return workingDay;
    }

}